                        <h5 class="text-justify">Versi Pertama ( Sumber : Buku 90 Tahun Kota Balikpapan yang mengutip buku karya F. Valenijn tahun 1724 )</h5>
                        <p style="color: #eeeeee;">Menurut legenda, asal nama Balikpapan adalah karena sebuah kejadian yang terjadi pada tahun 1739, sewaktu dibawah Pemerintahan Sultan Muhammad Idris dari Kerajaan Kutai, yang memerintahkan kepada pemukim-pemukim di sepanjang Teluk Balikpapan untuk menyumbang bahan bangunan guna pembangunan istana baru di Kutai lama. Sumbangan tersebut ditentukan berupa penyerahan sebanyak 1000 lembar papan yang diikat menjadi sebuah rakit yang dibawa ke Kutai Lama melalui sepanjang pantai. Setibanya di Kutai lama, ternyata ada 10 keping papan yang kurang (terlepas selama dalam perjalanan) dan hasil dari pencarian menemukan bahwa 10 keping papan tersebut terhanyut dan timbul disuatu tempat yang sekarang bernama "Jenebora". Dari peristiwa inilah nama Balikpapan itu diberikan (dalam istilah bahasa Kutai "Baliklah - papan itu" atau papan yang kembali yang tidak mau ikut disumbangkan).</p>
                    </div>
                </div>
                <div class="col-lg-6">
                    <div class="about_text text-justify">
                        <h5 class="text-justify">Versi Kedua ( Sumber : Legenda rakyat yang dimuat dalam buku 90 Tahun Kota Balikpapan )</h5>
                        <p style="color: #eeeeee;">Menurut legenda dari orang-orang suku Pasir Balik atau lazim disebut Suku Pasir Kuleng, maka secara turun menurun telah dihikayatkan tentang asal mula nama "Negeri Balikpapan". Orang-orang suku Pasir Balik yang bermukim di sepanjang pantai teluk Balikpapan adalah berasal dari keturunan kakek dan nenek yang bernama " KAYUN KULENG dan PAPAN AYUN ". Oleh keturunannya kampung nelayan yang terletak di Teluk Balikpapan itu diberi nama "KULENG - PAPAN" atau artinya "BALIK - PAPAN" (Dalam bahasa Pasir, Kuleng artinya Balik dan Papan artinya Papan) dan diperkirakan nama negeri Balikpapan itu adalah sekitar tahun 1527.</p>
